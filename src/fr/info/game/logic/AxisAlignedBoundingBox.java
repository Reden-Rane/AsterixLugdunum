package fr.info.game.logic;

public class AxisAlignedBoundingBox {

    public float x;
    public float y;
    public float width;
    public float height;

    public AxisAlignedBoundingBox(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
